/**
 * Create a raw mysql2 connection using provided config
 */
const createConnection = async (config, useDb = false) => {
  return mysql.createConnection({
    host: config.db_host,
    port: parseInt(config.db_port, 10) || 3306,
    user: config.db_user,
    password: config.db_password,
    ...(useDb ? { database: config.db_name } : {}),
    multipleStatements: true,
    charset: 'utf8mb4',
  });
};

/**
 * Split SQL file into logical phases, stripping comments
 */
const parseSqlPhases = (sql) => {
  // Remove single-line comments, keep structure
  const clean = sql
    .split('\n')
    .filter(line => !line.trim().startsWith('--'))
    .join('\n');

  // Split on semicolon, filter empty
  const statements = clean
    .split(';')
    .map(s => s.trim())
    .filter(s => s.length > 0);

  // Phase 1: CREATE DATABASE + USE — skip (we handle DB creation separately)
  // Phase 2: CREATE TABLE + ALTER TABLE
  // Phase 3: Base seeds — languages, currencies only
  // Phase 4: Skip default company, roles, modules, permissions, users, role_permissions — wizard handles these
  // Phase 5: FK constraints
  // Translation keys/translations — wizard handles these after company creation

  const schema = [];
  const baseSeeds = [];
  const fkConstraints = [];

  const skipPatterns = [
    /^CREATE\s+DATABASE/i,
    /^USE\s+/i,
    /^INSERT\s+INTO\s+`?(companies)/i,
    /^INSERT\s+INTO\s+`?(roles)/i,
    /^INSERT\s+INTO\s+`?(modules)/i,
    /^INSERT\s+INTO\s+`?(permissions)/i,
    /^INSERT\s+INTO\s+`?(users)/i,
    /^INSERT\s+INTO\s+`?(role_permissions)/i,
    /^INSERT\s+INTO\s+`?(settings)/i,
    /^INSERT\s+INTO\s+`?(translation_keys)/i,
    /^INSERT\s+INTO\s+`?(translations)/i,
    /^SET\s+@pwd/i,
    /^SET\s+SQL_SAFE_UPDATES/i,
    /^UPDATE\s+`?roles/i,
    /^UPDATE\s+`?permissions/i,
    /^UPDATE\s+`?translation_keys/i,
    /^SELECT\s+'/i,
  ];

  for (const stmt of statements) {
    const upper = stmt.trim().toUpperCase();

    // Skip unwanted statements
    if (skipPatterns.some(p => p.test(stmt.trim()))) continue;

    if (/^ALTER\s+TABLE/i.test(stmt)) {
      fkConstraints.push(stmt);
    } else if (/^INSERT\s+INTO\s+`?(languages|currencies)/i.test(stmt)) {
      baseSeeds.push(stmt);
    } else if (/^CREATE\s+TABLE/i.test(stmt)) {
      schema.push(stmt);
    }
  }

  return { schema, baseSeeds, fkConstraints };
};

