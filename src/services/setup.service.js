/**
 * Create the super admin user.
 * Also creates: Developer role (level 1000, no company) + Super Admin role (level 100).
 * Returns the created user object (without password).
 */
const createAdmin = async (data, companyId, config) => {
  let conn;
  try {
    conn = await createConnection(config, true);

    // ── Developer role (global — no company_id) ──────────────────────────────
    const [devRoleResult] = await conn.query(
      `INSERT INTO \`roles\` (name, slug, description, level, company_id, is_default, is_active, created_at, updated_at)
       VALUES ('Developer', 'developer', 'Full system access across all companies. Bypasses all checks.', 1000, NULL, 0, 1, NOW(), NOW())
       ON DUPLICATE KEY UPDATE level = 1000, is_active = 1`,
    );
    // Get developer role id (insert or existing)
    const [[devRole]] = await conn.query(
      `SELECT id FROM \`roles\` WHERE slug = 'developer' AND company_id IS NULL LIMIT 1`
    );
    const developerRoleId = devRole.id;

    // Update created_by on developer role to itself (convention)
    await conn.query(
      `UPDATE \`roles\` SET created_by = ? WHERE id = ? AND created_by IS NULL`,
      [developerRoleId, developerRoleId]
    );

    // ── Super Admin role (scoped to company) ─────────────────────────────────
    await conn.query(
      `INSERT INTO \`roles\` (name, slug, description, level, company_id, is_default, is_active, created_by, created_at, updated_at)
       VALUES ('Super Admin', 'super_admin', 'Full administrative access. Approves pending requests.', 100, ?, 0, 1, ?, NOW(), NOW())
       ON DUPLICATE KEY UPDATE level = 100, is_active = 1, company_id = VALUES(company_id)`,
      [companyId, developerRoleId]
    );

    const [[superAdminRole]] = await conn.query(
      `SELECT id FROM \`roles\` WHERE slug = 'super_admin' AND company_id = ? LIMIT 1`,
      [companyId]
    );
    const superAdminRoleId = superAdminRole.id;

    // ── Hash password ─────────────────────────────────────────────────────────
    const hashedPassword = await bcrypt.hash(data.password, 12);

    // ── Create super admin user ───────────────────────────────────────────────
    const [userResult] = await conn.query(
      `INSERT INTO \`users\`
        (full_name, email, password, role_id, company_id, avatar, login_access, email_verified_at, is_active, created_at, updated_at)
       VALUES (?, ?, ?, ?, ?, ?, 1, NOW(), 1, NOW(), NOW())`,
      [
        data.full_name,
        data.email,
        hashedPassword,
        superAdminRoleId,
        companyId,
        data.avatar || null,
      ]
    );

    const userId = userResult.insertId;

    // Update roles created_by to super admin user
    await conn.query(
      `UPDATE \`roles\` SET created_by = ? WHERE company_id = ? AND created_by IS NULL`,
      [userId, companyId]
    );

    // Update company created_by
    await conn.query(
      `UPDATE \`companies\` SET created_by = ? WHERE id = ?`,
      [userId, companyId]
    );

    // Update permissions + modules created_by
    await conn.query(
      `UPDATE \`permissions\` SET created_by = ? WHERE company_id = ? AND created_by IS NULL`,
      [userId, companyId]
    );
    await conn.query(
      `UPDATE \`modules\` SET created_by = ? WHERE company_id = ? AND created_by IS NULL`,
      [userId, companyId]
    );

    // ── Assign ALL permissions to Super Admin role ────────────────────────────
    await conn.query(
      `INSERT INTO \`role_permissions\` (role_id, permission_id, created_at, updated_at)
       SELECT ?, id, NOW(), NOW()
       FROM \`permissions\`
       WHERE company_id = ?
       ON DUPLICATE KEY UPDATE updated_at = NOW()`,
      [superAdminRoleId, companyId]
    );

    return {
      id: userId,
      full_name: data.full_name,
      email: data.email,
      role_id: superAdminRoleId,
      company_id: companyId,
    };
  } finally {
    if (conn) await conn.end();
  }
};

