/**
 * Run the schema SQL — creates all tables and inserts base seeds (language + currency).
 * Skips company/role/user/permission data — the wizard handles those dynamically.
 */
const runSchema = async (config) => {
  const rawSql = fs.readFileSync(SCHEMA_FILE, 'utf8');
  const { schema, baseSeeds, fkConstraints } = parseSqlPhases(rawSql);

  let conn;
  try {
    conn = await createConnection(config, true);

    // 1. Create tables
    for (const stmt of schema) {
      await conn.query(stmt);
    }

    // 2. Base seeds (language en, currency USD)
    for (const stmt of baseSeeds) {
      await conn.query(stmt);
    }

    // 3. FK constraints (may fail if already exist — ignore)
    for (const stmt of fkConstraints) {
      try {
        await conn.query(stmt);
      } catch (err) {
        if (err.code !== 'ER_DUP_KEY' && !err.message.includes('Duplicate')) {
          // Ignore duplicate FK errors on re-runs, rethrow others
          if (err.errno !== 1826 && err.errno !== 1025) throw err;
        }
      }
    }
  } finally {
    if (conn) await conn.end();
  }
};

